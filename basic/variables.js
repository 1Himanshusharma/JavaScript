// var that can be redefined
// let that can not redefined but update
// const that be update
var FullName = "himanshu Sharma";
console.log(FullName);
var FullName = "sharma";
console.log(FullName);

let Name = "Himanshu Sharma";
console.log(Name);
Name = "Sharma";
console.log(Name);


const MyName = "Himanshu Sharma";
console.log(MyName);
console.log(MyName);